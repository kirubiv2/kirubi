//Variables-containers used to hold values
//Declaring variables:1.let-It has block-level scope,which means it is limited to the block (like a function or a loop) where it is declared.2.const-Declares a constant value. Once assigned, a const variable cannot be reassigned.

let name="Prince"
console.log(name)

let height=1.65;
console.log(height)

height=1.76; //Reassigning the variable with a new value
console.log(height)

const city="Nairobi";
console.log(city)

city="Mombasa";// This one will bring an error. You cannot reassign a new value to a constant.
console.log(city)


